# Área de um Retângulo - Peça ao usuário para fornecer o comprimento e a largura de um retângulo. Use a fórmula Área = comprimento * largura para determinar a área e apresente o valor.
comprim = float (input ("Digite o comprimento do seu retângulo em cm"))
larg = float (input("Digite a largura do seu retângulo em cm"))
#aqui vai calcular a área
area = comprim*larg
print(f"A área do seu retângulo é: {area} cm")