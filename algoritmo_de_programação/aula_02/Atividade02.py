# Média de Nota Peça ao usuário quatro notas. Determine a média usando Média = (n1 + n2 + n3 + n4) / 4 e mostre a média obtida.
n1 = float(input("Digite sua primeira nota"))
n2 = float(input("Digite sua segunda nota"))
n3 = float(input("Digite sua terceira nota"))
n4 = float(input("Digite sua quarta nota"))
media=(n1+n2+n3+n4) / 4
print(f"A média de suas quatro notas é {media}")