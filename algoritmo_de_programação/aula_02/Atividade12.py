# Velocidade Média - Peça ao usuário o espaço percorrido em metros e o tempo gasto em segundos. Calcule a velocidade média. Apresente o resultado.
espaco = float(input("Digite o espaço percorrido em metros"))
tempo = float (input("Digite o tempo gasto em segundos"))

velocidade = espaco / tempo
print (f"A velocidade média é de {velocidade}m/s")